package classe;

public class Carro {

    private String modelo;
    private String marca;
    private String cor;
    private int ano;
    private int qtdeRodas;

    public Carro(String modelo, String marca, String cor, int ano, int qtdeRodas) {
        this.modelo = modelo;
        this.marca = marca;
        this.cor = cor;
        this.ano = ano;
        this.qtdeRodas = qtdeRodas;
    }

}
